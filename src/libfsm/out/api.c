static const struct fsm_state *
findany(const struct fsm_state *state)
{
	struct fsm_state *e, *f;
	struct set_iter it;
	int i;

	assert(state != NULL);

	f = set_first(state->edges[0].sl, &it);

	for (i = 0; i <= UCHAR_MAX; i++) {
		if (set_empty(state->edges[i].sl)) {
			return NULL;
		}

		for (e = set_first(state->edges[i].sl, &it); e != NULL; e = set_next(&it)) {
			if (e != f) {
				return NULL;
			}
		}
	}

	assert(f != NULL);

	return f;
}

void
fsm_out_api(const struct fsm *fsm, FILE *f,
	const struct fsm_outoptions *options)
{
	struct fsm_state *s, *e, *start;
	struct set_iter it;
	unsigned n;
	int end;

	assert(fsm != NULL);
	assert(f != NULL);
	assert(options != NULL);

/* TODO: option to output as a fragment */
/* TODO: use prefix for function name */
/* TODO: leaf callback for opaques */

	fprintf(f, "/* Generated by lx */\n");
	fprintf(f, "\n");

	fprintf(f, "#include LF_HEADER\n");
	fprintf(f, "\n");

	fprintf(f, "#include <stddef.h>\n");
	fprintf(f, "\n");

	fprintf(f, "#include <fsm/fsm.h>\n");
	fprintf(f, "\n");

	fprintf(f, "struct fsm *\n");
	fprintf(f, "f(void)\n");
	fprintf(f, "{\n");
	fprintf(f, "\tstruct fsm *fsm;\n");
	fprintf(f, "\tsize_t i;\n");
	fprintf(f, "\n");

	n = fsm_count(fsm, fsm_isany);
	fprintf(f, "\tstruct fsm_state *s[%u] = { 0 };\n", n);
	fprintf(f, "\n");

	fprintf(f, "\tfsm = fsm_new();\n");
	fprintf(f, "\tif (fsm == NULL) {\n");
	fprintf(f, "\t\treturn NULL;\n");
	fprintf(f, "\t}\n");
	fprintf(f, "\n");

	fprintf(f, "\tfor (i = 0; i < %u; i++) {\n", n);
	fprintf(f, "\t\ts[i] = fsm_addstate(fsm);\n");
	fprintf(f, "\t\tif (s[i] == NULL) {\n");
	fprintf(f, "\t\t\tgoto error;\n");
	fprintf(f, "\t\t}\n");
	fprintf(f, "\t}\n");
	fprintf(f, "\n");

	for (s = fsm->sl; s != NULL; s = s->next) {
		int i;

		{
			const struct fsm_state *a;

			a = findany(s);
			if (a != NULL) {
				fprintf(f, "\tfsm_addedge_any(fsm, s[%u], s[%u]);\n",
					indexof(fsm, s), indexof(fsm, a));
				continue;
			}
		}

		for (i = 0; i <= FSM_EDGE_MAX; i++) {
			for (e = set_first(s->edges[i].sl, &it); e != NULL; e = set_next(&it)) {
				assert(e != NULL);

				switch (i) {
				case FSM_EDGE_EPSILON:
					fprintf(f, "\tif (!fsm_addedge_epsilon(fsm, s[%u], s[%u])) { goto error; {\n",
						indexof(fsm, s), indexof(fsm, e));
					break;

				default:
					fprintf(f, "\tif (!fsm_addedge_literal(fsm, s[%u], s[%u], '",
						indexof(fsm, s), indexof(fsm, e));
					escputc(i, f);
					fprintf(f, "')) { goto error; }\n");
					break;
				}
			}
		}
	}

	fprintf(f, "\n");

	start = fsm_getstart(fsm);
	if (start == NULL) {
		return;
	}

	fprintf(f, "\tfsm_setstart(fsm, s[%u]);\n", indexof(fsm, start));

	for (s = fsm->sl; s != NULL; s = s->next) {
		if (fsm_isend(fsm, s)) {
			fprintf(f, "\tfsm_setend(fsm, s[%u], 1);\n", indexof(fsm, s));
		}
	}

	fprintf(f, "\n");
	fprintf(f, "\treturn fsm;\n");

	fprintf(f, "\n");
	fprintf(f, "error:\n");
	fprintf(f, "\n");
	fprintf(f, "\tfsm_free(fsm);\n");
	fprintf(f, "\n");
	fprintf(f, "\treturn NULL;\n");

	fprintf(f, "}\n");
	fprintf(f, "\n");
}

